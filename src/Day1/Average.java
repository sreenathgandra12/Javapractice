package Day1;
//Write a program take out the average of the 5 subjects marks
public class Average {
    public static void main(String[] args) {
        int marks1=10;
        int marks2=20;
        int marks3=30;
        int marks4=40;
        int marks5=50;
        int sum=marks1+marks2+marks3+marks4+marks5;
        int average = sum/5;
        System.out.println("Average :" + average);
    }
}
